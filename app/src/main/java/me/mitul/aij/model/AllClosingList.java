package me.mitul.aij.model;

public class AllClosingList {
    private String branchName;
    private int tfwsValue;
    private int openValue;
    private int ebcValue;
    private int sebcValue;
    private int scValue;
    private int stValue;

    public AllClosingList() {}

    public String getBranchName() {
        return branchName;
    }

    public void setBranchName(String p) {
        branchName = p;
    }

    public int getTfwsValue() {
        return tfwsValue;
    }

    public void setTfwsValue(int p) {
        tfwsValue = p;
    }

    public int getOpenValue() {
        return openValue;
    }

    public void setOpenValue(int p) {
        openValue = p;
    }

    public int getEbcValue() {
        return ebcValue;
    }

    public void setEbcValue(int p) {
        ebcValue = p;
    }

    public int getSebcValue() {
        return sebcValue;
    }

    public void setSebcValue(int p) {
        sebcValue = p;
    }

    public int getScValue() {
        return scValue;
    }

    public void setScValue(int p) {
        scValue = p;
    }

    public int getStValue() {
        return stValue;
    }

    public void setStValue(int p) {
        stValue = p;
    }
}
