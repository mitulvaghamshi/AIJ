package me.mitul.aij.model;

public class SplashScreen {
    private String text;
    private String textSize;
    private String textColor;

    public String getText() {
        return text;
    }

    public void setText(String p) {
        this.text = p;
    }

    public String getTextSize() {
        return textSize;
    }

    public void setTextSize(String p) {
        this.textSize = p;
    }

    public String getTextColor() {
        return textColor;
    }

    public void setTextColor(String p) {
        this.textColor = p;
    }
}
