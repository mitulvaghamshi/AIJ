package me.mitul.aij.model;

public class Common {
    private int id;
    private String name;
    private String address;
    private String city;

    public Common() {
    }

    public int getId() {
        return id;
    }

    public void setId(int p) {
        id = p;
    }

    public String getName() {
        return name;
    }

    public void setName(String p) {
        name = p;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String p) {
        address = p;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String p) {
        city = p;
    }
}
