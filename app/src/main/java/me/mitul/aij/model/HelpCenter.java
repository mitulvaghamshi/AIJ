package me.mitul.aij.model;

public class HelpCenter {
    private int helpCenterID;
    private String helpCenterName;
    private String helpCenterAddress;
    private String helpCenterCity;

    public HelpCenter() {
    }

    public int getHelpCenterID() {
        return helpCenterID;
    }

    public void setHelpCenterID(int p) {
        helpCenterID = p;
    }

    public String getHelpCenterName() {
        return helpCenterName;
    }

    public void setHelpCenterName(String p) {
        helpCenterName = p;
    }

    public String getHelpCenterAddress() {
        return helpCenterAddress;
    }

    public void setHelpCenterAddress(String p) {
        helpCenterAddress = p;
    }

    public String getHelpCenterCity() {
        return helpCenterCity;
    }

    public void setHelpCenterCity(String p) {
        helpCenterCity = p;
    }
}
